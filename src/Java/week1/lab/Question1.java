package Java.week1.lab;

public class Question1 {
    public static void main(String[] args) {
        // QUESTION 1
        // Define an Integer Variable
        int bankBalance;

        // Initialize bankBalance with a value of 500
        bankBalance = 500;

        // Perform Operations
        // Add 250 to the bankBalance
        bankBalance += 250;

        // Subtract 100 from the bankBalance
        bankBalance -= 100;

        // Print Resulting Value
        System.out.println("Resulting bank balance: " + bankBalance);

        // Define a Double Variable
        // Declaring a double variable named fahrenheit.
        double fahrenheit;

        // Assign fahrenheit an initial value between 0 and 100
        fahrenheit = 67.5;

        // Calculate Celsius
        // Declare another double variable named celsius
        double celsius;

        // Calculate the value of celsius based on the value of fahrenheit
        celsius = (fahrenheit - 32) * 5 / 9.0;

        // Print Final Value
        System.out.println("Celsius: " + celsius);
    }
}
